package inheritancedemo;

public class Phone {
	int cost=67570;
	String productName="Apple";
	
	public Phone() { //no arg constructor	
		System.out.println("one int arg constructor");
	}
	
	public Phone(String productName) {//one String arg constructor
		this();
		System.out.println("one String arg constructor	");
		this.productName = productName;
	}
	
	public Phone(int cost, String productName) {//two arg int, string constructor
		this("dfsf");
		System.out.println("two arg int, string constructor");
		this.cost = cost;
		this.productName = productName;
	}
	
	public static void main(String[] args) {
		Phone p=new Phone(4656,"dfdsf");
	}
}
